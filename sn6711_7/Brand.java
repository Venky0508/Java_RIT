package Homework_7;

//Class extended by two abstract classes
//Super Concrete Class : Brand
 class Brand {

    //Field Variable
    String brandName;
    
    //Constructor 
    public Brand(String brandName){
        this.brandName = brandName;
    }

     //Getter Method for brandName
     public String getBrandName(){
        return brandName;
    }

    //Setter Method for brandName
    public void setBrandName(){
        this.brandName = brandName;
    }
}

    

